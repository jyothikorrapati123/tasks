#binary
#implicit
a = 0b11111
print(a)
print(type(a))

#explicit
a= bin(0b11111)
print(a)
print(type(a))

#data type
a: bin = 0b11111
print(a)
print(type(a))

a= bin(10)
print(a)

a= bin(False)
print(a)

a= bin(True)
print(a)


